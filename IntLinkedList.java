public class IntLinkedList {  //[someInteger, IntLinkedList]
    private int info;
    private IntLinkedList next;

    public IntLinkedList(int info){  //[45,null]
        this.info = info;
        this.next =null;
    }

    public int getInfo(){
        return this.getInfo();
    }

    public void setInfo(int info){
        this.info=info;
    }

    public IntLinkedList getNext(){
        return this.next;
    }
    public void setNext(IntLinkedList node){
        this.next=node;
    }
    


}